function getDayOfYear(date: Date): number {
  const start = new Date(date.getFullYear(), 0, 0)
  const diff = date.getTime() - start.getTime()
  return Math.floor(diff / 86400000)
}

function rotateAndPick(items: string[], date: Date, limit: number): string[] {
  if (items.length <= limit) return items

  const seed = getDayOfYear(date)
  const start = seed % items.length
  const picked: string[] = []
  for (let i = 0; i < Math.min(limit, items.length); i++) {
    picked.push(items[(start + i) % items.length])
  }
  return picked
}

/**
 * 現在の月の旬食材を返す
 */
export function getCurrentSeasonalIngredients(limit = 10): string[] {
  const today = new Date()
  const month = today.getMonth() + 1
  const pool = seasonalMap[month] ?? []
  return rotateAndPick(pool, today, limit)
}
